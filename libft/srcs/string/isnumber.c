#include <unistd.h>

int		ft_isnumber(char *s)
{
	if (s == NULL || *s == '\0')
		return (0);
	if (*s == '-' && (*s + 1) != '\0')
		++s;
	while (*s != '\0' && *s >= '0' && *s <= '9')
		++s;
	if (*s == '\0')
		return (1);
	return (0);
}
